from __future__ import annotations

from hypothesis_factory.models import UncertaintyZone


def suggest_minimal_experiment(zone: UncertaintyZone, constraints: str = "") -> list[str]:
    entities = ", ".join(zone.linked_entities[:4]) or "выбранные факторы"
    limit = f" Учитывать ограничения: {constraints}" if constraints else ""

    def apply_limit(steps: list[str]) -> list[str]:
        if limit:
            steps[-1] = f"{steps[-1]}{limit}"
        return steps

    if zone.type == "coverage_gap":
        return apply_limit([
            f"Собрать 3-4 образца для непокрытой комбинации: {entities}.",
            "Измерить целевой KPI, дефектность и себестоимость в одной матрице испытаний.",
            "Сравнить с базовым режимом и зафиксировать критерий успеха.",
        ])
    if zone.type == "contradiction":
        return apply_limit([
            "Повторить конфликтующие условия в мини-DOE: разные концентрации и температуры.",
            "Использовать одинаковую методику измерения KPI для всех образцов.",
            "Определить, какое условие разворачивает знак эффекта.",
        ])
    if zone.type == "indirect_link":
        return apply_limit([
            f"Проверить прямую связь из графового пути: {' -> '.join(zone.indirect_path)}.",
            "Измерить промежуточный механизм и итоговый KPI на тех же образцах.",
            "Считать гипотезу перспективной только при согласованном росте механизма и KPI.",
        ])
    if zone.type == "mechanism_gap":
        return apply_limit([
            "Параллельно с KPI измерить микроструктуру, фазовый состав и размер выделений.",
            "Сопоставить изменение механизма с направлением изменения KPI.",
            "Отклонить гипотезу, если KPI меняется без воспроизводимого механизма.",
        ])
    if zone.type in {"high_extractable_loss", "coarse_locked_loss"}:
        return apply_limit([
            "Взять базовую пробу хвостов по целевому классу крупности и элементу.",
            "Проверить доизмельчение/классификацию на 2-3 режимах с замером распределения элемента 28/29.",
            "Считать успехом снижение массы целевого элемента в хвостах без деградации выхода концентрата.",
        ])
    if zone.type == "fine_particle_loss":
        return apply_limit([
            "Провести малый тест флотации тонкого класса с вариацией плотности пульпы, воды и времени агитации.",
            "Сравнить потери элемента 28/29 в хвостах с базовым режимом.",
            "Отобрать режим, где снижение потерь воспроизводится минимум в двух повторностях.",
        ])
    if zone.type == "missing_process_link":
        return apply_limit([
            "Сопоставить top-loss класс с участками схемы: измельчение, классификация, основная и контрольная флотация.",
            "Проверить один ближайший управляемый параметр оборудования для выбранного участка.",
            "Зафиксировать, появляется ли измеримое снижение потерь в том же классе крупности.",
        ])
    if zone.type == "expert_unvalidated":
        return apply_limit([
            "Сопоставить экспертную идею с численными зонами потерь по фабрике.",
            "Поставить минимальный A/B-тест экспертного изменения против базового режима.",
            "Считать гипотезу подтвержденной только при снижении потерь элемента 28/29 и понятном механизме.",
        ])
    return apply_limit([
        "Добавить бизнес/KPI-метрики: стоимость шихты, энергия, время печи и выход годного.",
        "Проверить технический эффект только в рамках допустимой экономики.",
        "Сравнить итоговую ценность с базовой технологией.",
    ])
